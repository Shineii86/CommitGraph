def text_to_pixels(text: str, start_week_offset: int = 0) -> list[tuple[int, int]]:
    """
    Convert a string into pixel coordinates for the contribution graph.

    Each character occupies 5 columns + 1 column spacing.
    Returns a list of (week_index, day_of_week) tuples.
    Row 0 = Sunday, Row 6 = Saturday.
    """
    text_upper = text.upper()
    pixels = []
    col = 0

    for ch in text_upper:
        if ch not in FONT_5x7:
            print(f"⚠️  Skipping unsupported character: '{ch}'")
            continue

        bitmap = FONT_5x7[ch]
        for row in range(7):        # 0=Sun .. 6=Sat
            for c in range(5):      # each column of the character
                if bitmap[row][c] == '1':
                    week = start_week_offset + col + c
                    day = row
                    pixels.append((week, day))
        col += 6  # 5 char width + 1 gap

    return pixels


def get_supported_chars() -> str:
    """Return a string of all supported characters."""
    return ''.join(sorted(FONT_5x7.keys()))
